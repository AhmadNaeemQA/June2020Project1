package solid_goldilocks;

public class goldilock {
	private int weight;
	private int max_temp;

	public goldilock(int weight, int max_temp) {
		this.weight = weight;
		this.max_temp = max_temp;
	}

	public goldilock() {

	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public int getMax_temp() {
		return max_temp;
	}

	public void setMax_temp(int max_temp) {
		this.max_temp = max_temp;
	}

}
